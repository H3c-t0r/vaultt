import Component from '@glimmer/component';

export default class IdentityList extends Component {
  get sortedItems() {
    const items = this.args.items;
    return items.sort((a, b) => {
      if (a.name < b.name) {
        return -1;
      }
      if (a.name > b.name) {
        return 1;
      }
      return 0;
    });
  }
}
